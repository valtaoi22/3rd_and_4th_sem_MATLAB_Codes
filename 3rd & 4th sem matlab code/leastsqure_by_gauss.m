clc;
N=5;
x=[-2,-1,0,1,2];
y=[15,1,1,3,19];
a(1,1)=N;
a(1,2)=0;
a(1,3)=0;
a(1,4)=0;
a(2,1)=0;
a(2,2)=0;
a(2,3)=0;
a(2,4)=0;
a(3,1)=0;
a(3,2)=0;
a(3,3)=0;
a(3,4)=0;
for i=1:N
    a(1,2)=a(1,2)+x(i);
    a(1,3)=a(1,3)+x(i)*x(i);
    a(1,4)=a(1,4)+y(i);
    a(2,1)=a(1,2);
    a(2,2)=a(1,3);
    a(2,3)=a(2,3)+(x(i))^3;
    a(2,4)=a(2,4)+x(i)*y(i);
    a(3,1)=a(2,2);
    a(3,2)=a(2,3);
    a(3,3)=a(3,3)+(x(i)^4);
    a(3,4)=a(3,4)+x(i)*x(i)*y(i);
end
b=[a(1,4);a(2,4);a(3,4)];
a=[a(1,1),a(1,2),a(1,3);a(2,1),a(2,2),a(2,3);a(3,1),a(3,2),a(3,3)];

A=[a b];
n=size(A,1);                                               
x=zeros(n,1);                                             
for i=1:n-1
    for j=i+1:n
        m = A(j,i)/A(i,i);
        A(j,:) = A(j,:) - m*A(i,:);
    end
end

x(n) = A(n,n+1)/A(n,n);

for i=n-1:-1:1
    summ = 0;
    for j=i+1:n
        summ = summ + A(i,j)*x(j,:);
        x(i,:) = (A(i,n+1) - summ)/A(i,i);
    end
end
