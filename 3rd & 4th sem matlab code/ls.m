clc;
N=5;
x=[-2,-1,0,1,2];
y=[15,1,1,3,19];
a(1,1)=N;
a(1,2)=0;
a(1,3)=0;
a(1,4)=0;
a(2,1)=0;
a(2,2)=0;
a(2,3)=0;
a(2,4)=0;
a(3,1)=0;
a(3,2)=0;
a(3,3)=0;
a(3,4)=0;
for i=1:N
    a(1,2)=a(1,2)+x(i);
    a(1,3)=a(1,3)+x(i)*x(i);
    a(1,4)=a(1,4)+y(i);
    a(2,1)=a(1,2);
    a(2,2)=a(1,3);
    a(2,3)=a(2,3)+(x(i))^3;
    a(2,4)=a(2,4)+x(i)*y(i);
    a(3,1)=a(2,2);
    a(3,2)=a(2,3);
    a(3,3)=a(3,3)+(x(i)^4);
    a(3,4)=a(3,4)+x(i)*x(i)*y(i);
end
B=[a(1,4);a(2,4);a(3,4)];
A=[a(1,1),a(1,2),a(1,3);a(2,1),a(2,2),a(2,3);a(3,1),a(3,2),a(3,3)];

tol=0.001;
n=3;
x=[0 0 0];

err = Inf;
while all(err > tol)
  x_old=x;
   for i=1:n
        sum=0;
        for j=1:i-1
            sum=sum+ A(i,j)*x(j);
        end
        for j=i+1:n
            sum= sum + A(i,j)*x_old(j);
        end
        x(i)= (B(i)-sum)/ A(i,i);
    end
    err= abs(x-x_old);
end
disp(x)
